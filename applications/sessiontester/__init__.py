from .. import basicapplib;
def main(session, args=[]):
    win = self.ui()
    self.con.write(self.windows)
    win.Label().set({"text": "Authenticated as: "+session.get("user")});
    win.Label().set({"text": "Session type: "+str(session.get("type"))});
self = basicapplib.Application(name="SessionChecker")
self.setScript(kind="main", program=main)
